    void ObtenerLista() {
        
         BookRepository repository = new BookRepository(url, user, password);
         
         List<Book> List = repository.findAll();
         
         DefaultTableModel dtm = new DefaultTableModel();
         String titulos[]={"ID","Titulo","Autor","Genero","Año"};
         dtm.setColumnIdentifiers(titulos);
         jtabla.setModel(dtm);
         
         // Borramos si tiene algo la tabla
          int cantFilas = dtm.getRowCount();
        if (cantFilas>0){
            for(int i=0;i<cantFilas;i++){
                dtm.removeRow(0);
            }
        }
        
        String datos[] = new String[10];
         
         for (Book book : List) {
                 datos[0] = String.valueOf(book.getId());
                 datos[1] = book.getTitle();
                 datos[2] = book.getAuthor();
                 datos[3] = book.getGenre();
                 datos[4] = String.valueOf(book.getYear());
                 dtm.addRow(datos);
         }
    }

