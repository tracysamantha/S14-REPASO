    private void jtablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtablaMouseClicked
          int fila = jtabla.getSelectedRow();
        String id =jtabla.getValueAt(fila, 0).toString();
        
        try {
            BookRepository repository = new BookRepository(url, user, password);
             
            repository.delete(Integer.parseInt(id));
            JOptionPane.showMessageDialog(rootPane, "Libro Eliminado");
            ObtenerLista();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al eliminar el libro");
        }
        
       
    }//GEN-LAST:event_jtablaMouseClicked

